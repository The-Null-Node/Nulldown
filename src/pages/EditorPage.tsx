import React, { useState, useEffect, useRef } from "react";
import useEditorStore, { type EditorState } from "../stores/editorStore"; // Import Zustand store and EditorState type
import EnhancedMarkdown from "../components/EnhancedMarkdown";
import MarkdownHelpers from "../components/MarkdownHelpers";

interface ShareApiResponse {
  id?: string;
  url?: string;
  error?: string; // If the API itself returns a structured error
  // Add any other fields the API might return on success/error
}

// const generateId = (title: string) => `${title !== undefined ? title.toLowerCase().replace(/ /g, '-').concat('-') : ""}${Math.random().toString(36).substring(2, 10)}`; // Not needed if ID is generated by backend

const EditorPage: React.FC = () => {
  // Use Zustand store for markdown content
  const markdown = useEditorStore((state: EditorState) => state.textContent);
  const setTextContent = useEditorStore((state: EditorState) => state.setTextContent);

  const [isClient, setIsClient] = useState(false);
  const [showPreview, setShowPreview] = useState(false);
  const [editorHidden, setEditorHidden] = useState(false);
  const [sharing, setSharing] = useState(false);
  const [error, setError] = useState<string | null>(null);
  const [successUrl, setSuccessUrl] = useState<string | null>(null);
  const textareaRef = useRef<HTMLTextAreaElement>(null);

  // Effect 1: Set isClient to true once after initial mount
  useEffect(() => {
    setIsClient(true);
  }, []); // Empty dependency array ensures this runs only once

  // Effect 2: Load from localStorage on initial client-side readiness
  useEffect(() => {
    if (isClient) {
      const savedContent = localStorage.getItem('nulldown_draft');
      if (savedContent !== null) { // Check specifically for null, as "" is a valid state
        // This will set the store content from localStorage once when the client is ready.
        // If the store was already populated by other means, this might overwrite it.
        // For this app, it's likely the intended initial hydration.
        setTextContent(savedContent);
      }
    }
    // This effect runs when isClient becomes true, or if setTextContent were to change (it's stable).
    // Effectively, it runs once on the client-side after the initial setup.
  }, [isClient, setTextContent]);

  // Effect 3: Save to localStorage whenever markdown content changes, but only on client
  useEffect(() => {
    if (isClient) {
      localStorage.setItem('nulldown_draft', markdown);
    }
    // This effect runs when markdown (from the store) changes, or when isClient becomes true.
  }, [markdown, isClient]);

  const handleShare = async () => {
    if (!markdown.trim()) {
      setError("Cannot share empty content.");
      return;
    }
    setSharing(true);
    setError(null);
    setSuccessUrl(null);
    try {
      // Call the Cloudflare Function API endpoint
      const response = await fetch("/api/store", {
        method: "POST",
        headers: {
          "Content-Type": "text/plain", // Send as plain text as per functions/api/store.ts
        },
        body: markdown,
      });

      if (!response.ok) {
        const errorData = await response.text();
        throw new Error(errorData || `Failed to create drop: ${response.statusText}`);
      }

      const result: ShareApiResponse = await response.json();
      if (result.id && result.url) {
        setSuccessUrl(result.url);
        setTextContent(""); // Clear editor on success (via Zustand)
        localStorage.removeItem('nulldown_draft'); // Clear draft
      } else {
        setError(result.error || "Failed to create drop. Unknown error from API.");
      }
    } catch (err: any) {
      console.error("Share error:", err);
      setError(err.message || "An unexpected error occurred while sharing.");
    } finally {
      setSharing(false);
    }
  };

  const copyToClipboard = (text: string) => {
    navigator.clipboard.writeText(text).catch(err => {
      console.error('Failed to copy: ', err);
      setError("Failed to copy link.");
    });
  };

  const newDrop = () => {
    setTextContent(""); // Clear via Zustand
    setSuccessUrl(null);
    setError(null);
    setShowPreview(false);
    setEditorHidden(false);
    localStorage.removeItem('nulldown_draft');
    textareaRef.current?.focus();
  };

  const insertText = (text: string) => {
    if (textareaRef.current) {
      const cursorPos = textareaRef.current.selectionStart;
      const textBefore = markdown.substring(0, cursorPos);
      const textAfter = markdown.substring(textareaRef.current.selectionEnd);
      
      // Add a newline before the inserted text if not at the beginning and the previous character is not a newline
      const prefix = cursorPos > 0 && textBefore.charAt(textBefore.length - 1) !== '\n' ? '\n\n' : '';
      // Add a newline after the inserted text if there's text after and it doesn't start with a newline
      const suffix = textAfter.length > 0 && textAfter.charAt(0) !== '\n' ? '\n\n' : '';
      
      const newText = textBefore + prefix + text + suffix + textAfter;
      setTextContent(newText);
      
      // Set focus back to textarea and move cursor after the inserted text
      setTimeout(() => {
        if (textareaRef.current) {
          textareaRef.current.focus();
          const newPosition = cursorPos + prefix.length + text.length + suffix.length;
          textareaRef.current.setSelectionRange(newPosition, newPosition);
        }
      }, 0);
    } else {
      // If ref not available, just append
      setTextContent(markdown + '\n\n' + text + '\n\n');
    }
  };

  // Toggle editor visibility
  const toggleEditorVisibility = () => {
    // If preview is hidden, we need to show it first
    if (!showPreview) {
      setShowPreview(true);
    }
    // Toggle editor visibility
    setEditorHidden(!editorHidden);
  };

  if (successUrl) {
    return (
      <main className="flex-1 p-4 flex items-center justify-center">
        <div className="max-w-md w-full bg-card border border-border rounded-md p-6">
          <h2 className="text-xl mb-4 text-accent">Drop Created</h2>
          <p className="mb-4 text-sm">Your markdown has been dropped into the void.</p>
          <div className="flex mb-4">
            <input
              type="text"
              value={successUrl}
              readOnly
              className="flex-1 bg-background border border-border border-r-0 rounded-l-md p-2 text-sm text-foreground focus:outline-none"
            />
            <button 
              onClick={() => copyToClipboard(successUrl)}
              className="bg-accent text-black hover:bg-accent-hover rounded-r-md px-4 py-2 text-sm font-medium transition-colors"
            >
              Copy
            </button>
          </div>
          <div className="flex justify-between">
            <button 
              onClick={newDrop}
              className="border border-accent text-accent hover:bg-accent/10 rounded-md px-4 py-2 text-sm font-medium transition-colors"
            >
              New Drop
            </button>
            {/* For Vite, direct <a> tag or Link from react-router-dom */}
            <a 
              href={successUrl} 
              target="_blank" 
              rel="noopener noreferrer"
              className="bg-accent text-black hover:bg-accent-hover rounded-md px-4 py-2 text-sm font-medium transition-colors inline-block"
            >
              Visit Drop
            </a>
          </div>
        </div>
      </main>
    );
  }

  // Very simple fixed layout
  return (
    <div className="fixed inset-0 flex flex-col">
      {/* Top controls */}
      <div className="py-4 px-4 border-b border-border bg-background flex justify-between items-center">
        <div className="text-sm">NULLDOWN</div>
        <div className="flex gap-2">
          <button 
            onClick={() => setShowPreview(!showPreview)}
            className="border border-accent text-accent hover:bg-accent/10 rounded-md px-4 py-2 text-sm font-medium transition-colors"
            disabled={editorHidden}
          >
            {showPreview ? "Hide Preview" : "Show Preview"}
          </button>
          
          <button 
            onClick={toggleEditorVisibility}
            className={`border ${editorHidden ? 'border-error-light text-error-light hover:bg-error/10' : 'border-accent text-accent hover:bg-accent/10'} rounded-md px-4 py-2 text-sm font-medium transition-colors`}
            disabled={!showPreview && editorHidden}
          >
            {editorHidden ? "Show Editor" : "Hide Editor"}
          </button>
          
          <button 
            onClick={handleShare}
            disabled={sharing || !markdown.trim()}
            className="bg-accent text-black hover:bg-accent-hover rounded-md px-4 py-2 text-sm font-medium transition-colors disabled:opacity-50 disabled:cursor-not-allowed"
          >
            {sharing && <span className="inline-block w-4 h-4 border-2 border-black/30 border-t-black rounded-full animate-spin mr-2"></span>}
            {sharing ? "Sharing..." : "Share to the Void"}
          </button>
        </div>
      </div>
      
      {/* Main content with absolute positioning to prevent layout shifts */}
      <div className="flex-1 flex flex-col md:flex-row relative" style={{ height: 'calc(100vh - 65px)' }}>
        {/* Error message - fixed position */}
        {error && (
          <div className="absolute top-2 left-4 right-4 z-10 bg-error/20 border border-error text-error-light p-3 rounded-md text-sm">
            {error}
          </div>
        )}
        
        {/* Editor */}
        {(!editorHidden || !showPreview) && (
          <div 
            className={`${showPreview && !editorHidden ? 'hidden md:block' : ''} absolute inset-0 p-4 md:w-1/2`}
            style={{ 
              display: showPreview && editorHidden ? 'none' : '',
              right: showPreview ? '50%' : '0'
            }}
          >
            <textarea
              ref={textareaRef}
              value={markdown}
              onChange={(e) => setTextContent(e.target.value)}
              placeholder="Write your markdown here..."
              className="w-full h-full resize-none bg-card border border-border rounded-md p-4 text-foreground font-mono focus:border-accent focus:outline-none"
              autoFocus
            />
          </div>
        )}
        
        {/* Preview */}
        {showPreview && (
          <div 
            className="absolute inset-0 p-4 overflow-auto bg-card border border-border rounded-md" 
            style={{ 
              left: editorHidden ? '0' : '50%',
              marginLeft: editorHidden ? '0' : '4px',
              marginRight: editorHidden ? '0' : '',
              right: '0'
            }}
          >
            <EnhancedMarkdown>
              {markdown || "*Preview will appear here*"}
            </EnhancedMarkdown>
          </div>
        )}
        
        {/* Markdown helpers - positioned at the bottom when shown */}
        {!editorHidden && !showPreview && (
          <div className="absolute bottom-0 left-0 right-0 p-4 bg-background z-10">
            <MarkdownHelpers onInsert={insertText} />
          </div>
        )}
      </div>
    </div>
  );
};

export default EditorPage; 